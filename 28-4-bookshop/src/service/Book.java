package service;

public class Book {
	private int bookNo;
	private int bookCodeNo;
	private int publisherNo;
	private String bookName;
	private String bookAuthor;
	private String bookPrice;
	private String bookPoint;
	private String bookAmount;
	private String bookOut;
	private String bookDate;
	
	public int getBookNo() {
		return bookNo;
	}
	public void setBookNo(int bookNo) {
		this.bookNo = bookNo;
	}
	public int getBookCodeNo() {
		return bookCodeNo;
	}
	public void setBookCodeNo(int bookCodeNo) {
		this.bookCodeNo = bookCodeNo;
	}
	public int getPublisherNo() {
		return publisherNo;
	}
	public void setPublisherNo(int publisherNo) {
		this.publisherNo = publisherNo;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookAuthor() {
		return bookAuthor;
	}
	public void setBookAuthor(String bookAuthor) {
		this.bookAuthor = bookAuthor;
	}
	public String getBookPrice() {
		return bookPrice;
	}
	public void setBookPrice(String bookPrice) {
		this.bookPrice = bookPrice;
	}
	public String getBookPoint() {
		return bookPoint;
	}
	public void setBookPoint(String bookPoint) {
		this.bookPoint = bookPoint;
	}
	public String getBookAmount() {
		return bookAmount;
	}
	public void setBookAmount(String bookAmount) {
		this.bookAmount = bookAmount;
	}
	public String getBookOut() {
		return bookOut;
	}
	public void setBookOut(String bookOut) {
		this.bookOut = bookOut;
	}
	public String getBookDate() {
		return bookDate;
	}
	public void setBookDate(String bookDate) {
		this.bookDate = bookDate;
	}
}
